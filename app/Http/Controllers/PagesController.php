     */
    public function show(Request $request)
    {
        $pageID = (int)$request->input('id');
        $model = new Pages();
        $page = $model->getPageByID($pageID);
        if ($page == null) {
            abort(404);
        }
        $title = $page->title;
        $body = $page->body;
        return view('pages.show', compact('title','body'));

    }


